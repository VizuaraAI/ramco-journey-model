"""Migrate the 50 existing eval cases to the new ground-truth schema.

For each case:
  1. Read the existing case JSON
  2. Derive the ground_truth block by reading the per-turn `expected` blocks
     and looking up SP chains + table footprints in the parsed catalog / SPs
  3. Write to eval/cases_gt/<category>/<id>.json (parallel tree)

After we're happy with the migrated cases, the old eval/cases/ tree can be
retired.
"""
from __future__ import annotations
import json
import shutil
from collections import defaultdict
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
EVAL = ROOT / "eval"
SRC = EVAL / "cases"
DST = EVAL / "cases_gt"
PARSED = ROOT / "out" / "parsed"

CATALOG = json.loads((PARSED / "service_catalog.json").read_text())
SP_DATA = json.loads((PARSED / "sp_branches.json").read_text())

# Upper-case activity ↔ camel-case used in catalog
ACT_MAP = {
    "PoCrt": "PoCrt", "PoCrtQtn": "PoCrtQtn", "PoCrtSo": "PoCrtSo",
    "PoCrtTen": "PoCrtTen", "PoCopy": "PoCopy", "PoAmnd": "PoAmnd",
    "PoApp": "PoApp", "PoEdt": "PoEdt", "PoViw": "PoViw",
    "PoMtn": "PoMtn", "PoHold": "PoHold", "PoScl": "PoScl",
    "PoAcCcUsgMod": "PoAcCcUsgMod", "PoHlp": "PoHlp",
}


def find_ui_for_task(activity_camel: str, task_name: str) -> str | None:
    prefix = activity_camel + "|"
    for key in CATALOG["chains"]:
        if not key.startswith(prefix):
            continue
        _, ui, t = key.split("|", 2)
        if t.lower() == task_name.lower():
            return ui
    return None


def sp_chain_for(activity_camel: str, ui: str, task: str) -> list[str]:
    prefix = f"{activity_camel}|{ui}|"
    target = task.lower()
    for key, chain in CATALOG["chains"].items():
        if not key.startswith(prefix):
            continue
        actual = key[len(prefix):]
        if actual.lower() == target:
            return [s["spname"] for s in chain if s.get("spname")]
    return []


def _is_staging(name: str) -> bool:
    if "tmp" in name or "temp" in name: return True
    if "_srch" in name: return True
    return False


def tables_for_chain(chain: list[str]) -> list[str]:
    """Real persistence tables only. Mirrors gt_evaluator's filter."""
    tables = set()
    sps = SP_DATA["sps"]
    for sp_name in chain:
        sp = sps.get(sp_name.lower())
        if not sp:
            continue
        for b in sp.get("branches", []):
            cons = b.get("consequences", {})
            for t in cons.get("inserts", []): tables.add(t.lower())
            for t in cons.get("updates", []): tables.add(t.lower())
            for t in cons.get("deletes", []): tables.add(t.lower())
    return sorted(t for t in tables
                  if not _is_staging(t)
                  and len(t) > 3
                  and t not in ("dtl", "data"))


# Slots that are SYSTEM OUTPUTS (assigned by the SP chain, not entered by user).
# These must not appear in `slot_values_required` — the bot can't provide them
# because they don't exist until after the commit fires.
OUTPUT_SLOTS = {
    "po_number",       # assigned by numbering series on create
    "approval_date",   # stamped on approve
    "exchange_rate",   # computed by erate SP
    "amendment_number",
    "workflow_status",
    "audit_id",
}


def collect_slots_from_turns(case: dict) -> dict:
    """Merge every slot_values_extracted from per-turn expected blocks.
    Excludes OUTPUT_SLOTS (auto-generated by system, not user input)."""
    merged = {}
    for turn in case.get("conversation", []):
        exp = turn.get("expected", {})
        for k, v in (exp.get("slots_extracted") or {}).items():
            if k in OUTPUT_SLOTS: continue
            if v not in (None, "", "?"):
                merged[k] = v
    for turn in case.get("conversation", []):
        exp = turn.get("expected", {})
        for k, v in (exp.get("context_carried") or {}).items():
            if k in OUTPUT_SLOTS: continue
            if v not in (None, ""):
                merged.setdefault(k, v)
    term = case.get("expected_terminal_state", {})
    for s in term.get("slots_filled", []):
        if s in OUTPUT_SLOTS: continue
        merged.setdefault(s, "ANY")
    return merged


def collect_trans_sequence(case: dict) -> list[dict]:
    """Walk the conversation in order, pick up every trans_invoked + cross_module_query."""
    trans_seq = []
    for turn in case.get("conversation", []):
        exp = turn.get("expected", {})
        t = exp.get("trans_invoked")
        if t:
            trans_seq.append({"task": t, "turn": turn["turn"]})
    return trans_seq


def find_activity_for_task(task_name: str, candidate_activities: list[str]) -> tuple[str, str] | None:
    """For a TRANS task, find which of the candidate activities (camel-case)
    actually contains it. Returns (activity_camel, ui_name) or None."""
    for camel in candidate_activities:
        ui = find_ui_for_task(camel, task_name)
        if ui:
            return camel, ui
    return None


def derive_ground_truth(case: dict) -> dict:
    cat = case.get("category", "")
    journeys = case.get("covers_journeys", [])
    candidate_activities_camel = [ACT_MAP.get(j) for j in journeys if ACT_MAP.get(j)]

    trans_seq = collect_trans_sequence(case)
    merged_slots = collect_slots_from_turns(case)
    term = case.get("expected_terminal_state", {})
    status_after = term.get("po_status", "")

    # Classify based on what tasks actually appear, not on category label alone.
    # We need to know which trans are reads (entry-screen searches) vs writes,
    # so do a preliminary pass to count them.
    n_write_like = 0
    n_read_like = 0
    for entry in trans_seq:
        t = entry["task"]
        found = find_activity_for_task(t, candidate_activities_camel)
        if not found:
            n_write_like += 1  # unknown, assume write
            continue
        act_c, ui_n = found
        chain = sp_chain_for(act_c, ui_n, t)
        tables = tables_for_chain(chain)
        is_read = (t.upper().endswith("ENTTRN1") or
                   (act_c == "PoViw" and t.upper().startswith("POVW")) or
                   (chain and not tables))
        if is_read: n_read_like += 1
        else:       n_write_like += 1

    if cat == "discovery" and n_write_like == 0:
        kind = "discovery"
    elif cat == "error-recovery" and not term.get("commit_task"):
        kind = "error_no_commit"
    elif n_write_like >= 2:
        kind = "multi_commit"
    elif n_write_like == 1:
        kind = "commit"
    elif n_read_like >= 1:
        kind = "lookup"
    else:
        kind = "discovery"

    writes = []
    reads = []
    no_writes = False

    for entry in trans_seq:
        task = entry["task"]
        # Find the right activity for THIS trans (cross-journey cases span activities)
        found = find_activity_for_task(task, candidate_activities_camel)
        if found:
            activity_camel, ui = found
            upper_act = activity_camel.upper()
        else:
            activity_camel, ui, upper_act = None, None, ""
        chain = sp_chain_for(activity_camel, ui, task) if (activity_camel and ui) else []
        tables = tables_for_chain(chain)

        # Heuristic: "read" tasks are entry-screen searches (ENTTRN1) and
        # the View activity's search. Tasks that have a chain but touch zero
        # tables are also reads.
        is_read = (
            task.upper().endswith("ENTTRN1") or
            (upper_act == "POVIW" and task.upper() == "POVWENTTRN1") or
            (chain and not tables)
        )

        if is_read:
            filter_slots = {k: v for k, v in merged_slots.items()
                           if k in ("po_number", "supplier_code", "date_from", "date_to",
                                    "status_filter", "item_code", "buyer", "quotation_no",
                                    "sale_order_no", "tender_no", "source_po_no", "pr_number")}
            reads.append({
                "task": task,
                "activity": upper_act,
                "filter_slots": filter_slots,
                "returns_fields": [],
            })
        else:
            writes.append({
                "task": task,
                "activity": upper_act,
                "screen": ui or "",
                "via_sp_chain": chain,
                "tables_required": tables,
                "slot_values_required": dict(merged_slots),
                "status_after": status_after,
            })

    # Discovery / pure-no-commit cases must NOT carry writes
    if kind in ("discovery", "error_no_commit"):
        no_writes = True
        writes = []
    elif kind == "lookup":
        no_writes = True
        writes = []
    # commit / multi_commit cases CAN have reads (e.g. entry-screen search
    # before the commit) — keep both

    # Discovery cases: list the must-mention information from bot_must clauses
    must_enumerate = []
    if kind == "discovery":
        for turn in case.get("conversation", []):
            for m in (turn.get("expected", {}).get("bot_must") or []):
                # Anything that says "enumerate" / "list" / "explain" is an info requirement
                if any(w in m.lower() for w in ("enumerate", "list", "explain", "describe", "mention")):
                    must_enumerate.append(m)

    cross_module = []
    for turn in case.get("conversation", []):
        exp = turn.get("expected", {})
        cmq = exp.get("cross_module_query")
        if cmq:
            cross_module.append({
                "module": (case.get("covers_modules") or ["?"])[-1],
                "effect": cmq,
            })

    gt = {
        "kind": kind,
        "no_writes_expected": no_writes,
        "status_after": status_after,
    }
    if writes:           gt["writes"] = writes
    if reads:            gt["reads"] = reads
    if cross_module:     gt["cross_module_writes"] = cross_module
    if must_enumerate:   gt["must_enumerate_information"] = must_enumerate
    return gt


def slim_conversation(case: dict) -> list[dict]:
    """Keep only `turn` and `user` — drop per-turn `expected` block."""
    return [{"turn": t["turn"], "user": t["user"]} for t in case.get("conversation", [])]


def migrate_case(case_path: Path) -> dict:
    case = json.loads(case_path.read_text(encoding="utf-8"))
    new_case = {
        "id": case["id"],
        "title": case["title"],
        "covers_journeys": case.get("covers_journeys", []),
        "covers_modules":  case.get("covers_modules", ["PO"]),
        "category": case["category"],
        "difficulty": case.get("difficulty", "medium"),
        "user_persona": case.get("user_persona", ""),
        "conversation": slim_conversation(case),
        "ground_truth": derive_ground_truth(case),
    }
    if case.get("notes"):
        new_case["notes"] = case["notes"]
    return new_case


def main():
    if DST.exists():
        shutil.rmtree(DST)
    DST.mkdir(parents=True)

    total = 0
    by_cat_summary = defaultdict(lambda: {"commit": 0, "lookup": 0, "discovery": 0,
                                          "error_no_commit": 0, "multi_commit": 0})
    for case_path in sorted(SRC.rglob("EVAL-*.json")):
        new_case = migrate_case(case_path)
        cat_dir = DST / case_path.parent.name
        cat_dir.mkdir(exist_ok=True)
        out_path = cat_dir / case_path.name
        out_path.write_text(json.dumps(new_case, indent=2), encoding="utf-8")
        total += 1
        by_cat_summary[case_path.parent.name][new_case["ground_truth"]["kind"]] += 1

    print(f"Migrated {total} cases → {DST.relative_to(ROOT)}/")
    print("\nGround-truth kind by category:")
    print(f"  {'category':25s}  commit  multi  lookup  discov  no_cmt")
    for cat, kinds in sorted(by_cat_summary.items()):
        print(f"  {cat:25s}  {kinds['commit']:6d}  "
              f"{kinds['multi_commit']:5d}  {kinds['lookup']:6d}  "
              f"{kinds['discovery']:6d}  {kinds['error_no_commit']:6d}")


if __name__ == "__main__":
    main()
